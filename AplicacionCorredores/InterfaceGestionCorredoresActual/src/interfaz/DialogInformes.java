    private void jButtonGenerarInforme1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGenerarInforme1ActionPerformed

        try {
            //Obtenemos la lista a mostrar
            List<Carrera> listaCarreras = new ArrayList<Carrera>();
            List<Carrera> listaCarrerasSinFinalizar = LogicaNegocio.getInstance().getListaCarreras();

            for (Iterator<Carrera> iterator = listaCarrerasSinFinalizar.iterator(); iterator.hasNext();) {
                Carrera carreraSinFinalizar = iterator.next();

                if (carreraSinFinalizar.getEstado().equalsIgnoreCase("Pendiente")) {
                    listaCarreras.add(carreraSinFinalizar);
                }

            }

//La encapsulamos en el objeto adecuado
            JRDataSource dataSource = new JRBeanCollectionDataSource(listaCarreras);

//Creamos el map para los parámetros, en este caso va vacío.
            Map parametros = new HashMap();
            JasperPrint print = JasperFillManager.fillReport(
                    "informe1.jasper", parametros, dataSource);

            JasperExportManager.exportReportToPdfFile(print, carpetaInformes.
                    getAbsolutePath() + File.separator + "Informe1.pdf");

        } catch (JRException ex) {
            Exceptions.printStackTrace(ex);
        }
        //this.dispose();
        JOptionPane.showMessageDialog(this, "Se ha generado el informe satisfactoriamente.",
                "Informe 1.", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButtonGenerarInforme1ActionPerformed

