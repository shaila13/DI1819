    private void jButtonSeleccionarRutaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSeleccionarRutaActionPerformed

        JFileChooser chooser = new JFileChooser();
        chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        chooser.setMultiSelectionEnabled(false);
        int returnSelection = chooser.showOpenDialog(null);
        if (returnSelection == JFileChooser.APPROVE_OPTION) {
            seleccionUnidadGuardarInforme = chooser.getSelectedFile().getAbsolutePath();
            carpetaInformes = new File(seleccionUnidadGuardarInforme + File.separator + "informes_Ireport");
            if (!carpetaInformes.exists()) {
                carpetaInformes.mkdir();
                seleccionUnidadGuardarInforme = carpetaInformes.getAbsolutePath();
            } else {
//Poner que seleccione la carpeta
                seleccionUnidadGuardarInforme = carpetaInformes.getAbsolutePath();
            }
        }
        jButtonGenerarInforme1.setEnabled(true);
        jButtonGenerarInforme2.setEnabled(true);
        jButtonGenerarInforme3.setEnabled(true);
        jButtonGenerarInforme4.setEnabled(true);
        jLabelOpcion.setVisible(true);

        jButtonSeleccionarRuta.setEnabled(false);
        jLabelSelecionarRuta.setEnabled(false);

    }//GEN-LAST:event_jButtonSeleccionarRutaActionPerformed

